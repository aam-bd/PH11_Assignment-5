const complete_btn = document.querySelector("#fix-mobile-btn");
complete_btn.addEventListener("click", function(){
const task = document.querySelector("#task-to-complete");
const value_task1 = task.innerText;
const new_value = Number(value_task1) - 1;
task.innerText = new_value;
const task_completed = document.querySelector("#task-completed");
const value_t = task_completed.innerText;
const new_v = Number(value_t) + 1;
task_completed.innerText = new_v;
alert("Board Updated Successfully");
complete_btn.setAttribute("disabled", "true")
});

